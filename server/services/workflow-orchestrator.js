  /**
   * Determine file type from extension
   */
  getFileType(filename) {
    const ext = filename.split('.').pop().toLowerCase();
    const typeMap = {
      'js': 'javascript',
      'jsx': 'javascript', 
      'ts': 'typescript',
      'tsx': 'typescript',
      'html': 'html',
      'css': 'css',
      'json': 'json',
      'md': 'markdown',
      'txt': 'text',
      'py': 'python',
      'yml': 'yaml',
      'yaml': 'yaml'
    };
    return typeMap[ext] || 'unknown';
  }
